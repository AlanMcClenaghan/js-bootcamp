let fahrenheit = 32;

// Calculate celsius from fahrenheit
//(32°F − 32) × 5/9 = 0°C
let celsius = (fahrenheit - 32) * 5/9;

// Calculate kelvin from fahrenheit
//(32°F − 32) × 5/9 + 273.15 = 273.15K
let kelvin = (fahrenheit - 32) * 5/9 + 273.15;

console.log(fahrenheit);
console.log(celsius);
console.log(kelvin);